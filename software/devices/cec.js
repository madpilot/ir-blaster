function buildObjects(names) {
  var devices = {};

  for(var i = 0; i < names.length; i++) {
    if(names[i]) {
      var name = names[i];
      var receiver = new CECDevice(name);
      devices["name-" + name] = receiver;
    }
  }
  return devices;
}

